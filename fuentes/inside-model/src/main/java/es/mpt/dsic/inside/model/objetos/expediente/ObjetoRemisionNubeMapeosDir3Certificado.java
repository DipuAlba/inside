package es.mpt.dsic.inside.model.objetos.expediente;

public class ObjetoRemisionNubeMapeosDir3Certificado {

  private Integer id;
  private String dir3Padre;
  private String serialNumber;
  private String nombre;
  private String apellidos;
  private String telefono;
  private String email;

  public String getDir3Padre() {
    return dir3Padre;
  }

  public void setDir3Padre(String dir3Padre) {
    this.dir3Padre = dir3Padre;
  }

  public String getSerialNumber() {
    return serialNumber;
  }

  public void setSerialNumber(String serialNumber) {
    this.serialNumber = serialNumber;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

  public String getApellidos() {
    return apellidos;
  }

  public void setApellidos(String apellidos) {
    this.apellidos = apellidos;
  }

  public String getTelefono() {
    return telefono;
  }

  public void setTelefono(String telefono) {
    this.telefono = telefono;
  }

  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }



}
